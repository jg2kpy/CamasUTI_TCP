    DefaultTableModel modelGlobal;
    
    public void setDatos(String estadoActual){
        DefaultTableModel model = (DefaultTableModel) table.getModel(); 
        modelGlobal = model;
        int n = estadoActual.length();
        this.txtTotal.setText(Integer.toString(n));
        this.cbCamas.addItem("");
        for(int i=0;i<n;i++){
            if(estadoActual.charAt(i)=='T'){
                this.cbCamas.addItem(Integer.toString(i));
                model.addRow(new Object[]{i,true});
            }else{
                this.cbCamas.addItem(Integer.toString(i));
                model.addRow(new Object[]{i,false});
            }
        }  
    }

