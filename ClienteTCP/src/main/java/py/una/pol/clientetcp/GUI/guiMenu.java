    private void btnMasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasActionPerformed
        int n = Integer.parseInt(this.txtTotal.getText());
        n++;
        Menu.variacion++;
        this.txtTotal.setText(Integer.toString(n));
        modelGlobal.addRow(new Object[]{n,false});
        Menu.estadoActual = Menu.estadoActual + "F";
        this.cbCamas.addItem(Integer.toString(n));
    }//GEN-LAST:event_btnMasActionPerformed

    private void btnMenosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMenosActionPerformed
        int n = Integer.parseInt(this.txtTotal.getText());        
        if(n!=0){
            int respuesta = 0;
            if(Menu.estadoActual.charAt(n-1)=='T'){
                respuesta = JOptionPane.showConfirmDialog(null, "Esta apunto de eliminar una cama ocupada, esta seguro?","Peligro",JOptionPane.YES_NO_OPTION);
            }
            if(respuesta==0){
                Menu.variacion--;
                n--;
                this.txtTotal.setText(Integer.toString(n));
                modelGlobal.removeRow(n);
                this.cbCamas.removeItemAt(n+1);
                Menu.estadoActual = Menu.estadoActual.substring(0,n);               
            }
        }        
    }//GEN-LAST:event_btnMenosActionPerformed

