    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if(!this.cbCamas.getSelectedItem().equals("")){
            int pos = Integer.parseInt((String) this.cbCamas.getSelectedItem());
            Menu.aCambiar = pos;
            if(Menu.estadoActual.charAt(pos)=='T'){
                Menu.ocupar = false;
                Menu.estadoActual = Menu.estadoActual.substring(0,pos) + "F" + Menu.estadoActual.substring(pos+1);
            }else{
                Menu.ocupar = true;
                Menu.estadoActual = Menu.estadoActual.substring(0,pos) + "T" + Menu.estadoActual.substring(pos+1);
            }
        }            
    }//GEN-LAST:event_jToggleButton1ActionPerformed

